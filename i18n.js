    const inheritance = {
        mataro: 'viladomat',
        santgervasi: 'viladomat',
        balmes: 'viladomat'
    };

    function getPageDict(page) {
        const common = translations[currentLang] || translations.es;
        const pageData = common[page] || {};
        return new Proxy(pageData, {
            get(target, prop) {
                if (prop in target) return target[prop];
                const parent = inheritance[page];
                if (parent && common[parent] && prop in common[parent]) {
                    return common[parent][prop];
                }
                return common[prop];
            }
        });
    }

